Name = "john"
age = 20 
balence = 0
wallet = 100.50
working = True

print(type(Name)) # maka dia akan menjadi <class 'str'>
print(type(age)) # maka dia akan menjadi <class 'int'>
print(type(wallet)) # maka dia akan menjadi <class 'float'>
print(type(working)) # maka dia akan menjadi <class 'bool'>

print(bool(balence)) # maka dia akan menjadi false karna tidak ada nilai nya
print(bool(wallet)) # maka dia akan menjadi true karna ada nilai nya
print(bool(working)) # maka dia akan menjadi true karna ada nilai nya
